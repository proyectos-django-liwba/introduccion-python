num1 = 20

num2 = 30

# Solo se puede sumar numeros
print(num1 + num2)

# Resta
print(num1 - num2)

# Multiplicacion
print(num1 * num2)

# Division
print(num1 / num2)

# Modulo
print(num1 % num2)

# Potencia
print(num1 ** num2)

# division entera
print(num1 // num2)

# indicar orden de operaciones
print((num1 + num2) * 2)
print(num1 + num2 * 2)

